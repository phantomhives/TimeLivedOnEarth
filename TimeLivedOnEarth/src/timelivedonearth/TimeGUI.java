    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        
        Date Date_of_Birth = null;
        Date Date_of_today = null;
        
        try {
            Date_of_Birth = format.parse(format.format(jDateChooser2.getDate()));
            Date_of_today = format.parse(format.format(jDateChooser1.getDate()));
        } 
        catch (Exception e)
        {
            e.printStackTrace();
        }
        
        long DateDiff = Date_of_Birth.getTime() - Date_of_today.getTime();
        long hours = DateDiff / (60 * 60 * 1000);
        long minutes = hours * 60;
        long Seconds = minutes * 60;
        long days = hours / 24 ;
        long weeks = days / 7 ;
        int diffYear = (int) ((DateDiff / 1000 / 60 / 60 / 24) /365);
            
        
        YearBox.setText(Long.toString(diffYear));
        WeeksBox.setText(Long.toString(weeks));
        DaysBox.setText(Long.toString(days));
        HourBox.setText(Long.toString(hours));
        MitunsBox.setText(Long.toString(minutes));
        SecondBox.setText(Long.toString(Seconds));
       
    }//GEN-LAST:event_jButton1ActionPerformed

